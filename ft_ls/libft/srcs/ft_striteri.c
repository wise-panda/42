#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int i;

	i = 0;
	while (&s[i] && s[i])
	{
		if (f)
			f(i, &s[i]);
		i++;
	}
}
